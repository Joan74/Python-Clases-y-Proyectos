contador = 1  # variable de contador que es igual a 1
while contador <= 61:  # Muestra contador menor o igual a 60
    cuadrado = contador*contador  # Multiplicacion es igual a contador por contador
    # Imprime el numero y la multiplicacion
    print(f"el cuadrado de {contador} es {cuadrado}")
    contador += 1  # al contador se suma 1 a cada bucle

for numero in range(61):  # muesrta un rango hasta el 60
    cuadrado = numero*numero  # multiplicacion es igual al numero por el numero
    print(f" {numero} {cuadrado}")  # imprime el numero y la multiplicacion
